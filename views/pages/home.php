    <section id="activite">
        <div class="section-title">
            <h2 class=titreH2AvecFond>Nos activités</h2>
        </div>
        <div class="divPresentation">
            <div class="presentationDescription2">
                <p>La low-tech nous semble être un outil majeur pour la transition écologique et énergétique, il est
                    donc nécessaire que le plus de monde possible soit informé sur ce que c'est et quelles sont ses
                    applications.
                    <br>
                    <br>
                    À cette fin, nous pouvons intervenir pour une large variété de publics, allant des scolaires aux
                    salariés d'entreprises dans des ateliers dédiés, en passant par les stands d'exposition et les
                    conférences ou tables rondes.
                    <br>
                    <br>
                    Notre atelier est lui aussi tourné vers la transmission des connaissances et de l'apprentissage de
                    la démarche low-tech. Pour ce faire, nous organisons régulièrement des stages sur une thématique
                    spécifique, et nous mettons petit à petit en place un catalogue de formation couvrant diverses
                    thématiques pour augmenter votre autonomie et faire des économies. C'est aussi un lieu où se
                    retrouvent les adhérents pour co-construire les projets de l'association et participer à ses
                    missions d'intérêt général.
                    <br>
                    <br>
                    Les low-techs que nous fabriquons sont conçues, construites et testées par le bureau de Recherche &
                    Développement de l'association, pour être capable de vous fournir des données d'utilisation, de
                    mieux comprendre le fonctionnement des low-techs fabriquées, et adapter les solutions aux besoins
                    spécifiques du territoire. Toutes les low-techs proposées par l'association sont donc réellement
                    utiles, elles participent concrètement à la résilience et l'autonomie des personnes et des
                    structures collectives.
